package com.practise.basic.escapecharacter;

public class Main {
    public static void main(String[] args) {
        // excape '\'
        // \t is used for tab space
        System.out.println("today is \t sunday");
        // \n
        System.out.println("i am gd\n what about u");
        System.out.println("i am gd \"john\" what about u");
        System.out.println("\'");
        System.out.println("john \\ wick");
        int y=10;
        System.out.println(y--);
        System.out.println(y);;
    }
}
